# Conditional Tests
a = 41

# ==
if a == 41:
    print(bool(a))
else:
    print(a)


#  !=
if a != 2:
    print(bool(a))
else:
    print(a)


# >
if a > 20:
    print(bool(a))
else:
    print(a)


# >=
if a >= 52:
    print(bool(a))
else:
    print(a)


# <
if a < 8:
    print(bool(a))
else:
    print(a)


# <=
if a <= 41:
    print(bool(a))
else:
    print(a)